package org.springframework.cloud.stream.app.pg.cdc.keyvalue;

import org.springframework.boot.context.properties.ConfigurationProperties;

/**
 * @author Christian Tzolov
 */
@ConfigurationProperties("pg.cdc.keyvalue")
public class KeyValueProperties {
    /**
     * JDBC URL to connect to the Postgres DB.
     */
    private String jdbcUrl = "jdbc:postgresql://localhost:5432/postgres";

    /**
     * JDBC user name
     */
    private String jdbcUser = "postgres";

    /**
     * JDBC password
     */
    private String jdbcPassword = "postgres";

    public String getJdbcUrl() {
        return jdbcUrl;
    }

    public void setJdbcUrl(String jdbcUrl) {
        this.jdbcUrl = jdbcUrl;
    }

    public String getJdbcUser() {
        return jdbcUser;
    }

    public void setJdbcUser(String jdbcUser) {
        this.jdbcUser = jdbcUser;
    }

    public String getJdbcPassword() {
        return jdbcPassword;
    }

    public void setJdbcPassword(String jdbcPassword) {
        this.jdbcPassword = jdbcPassword;
    }
}
